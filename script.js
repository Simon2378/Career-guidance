function showDepartment(dept) {
  const detailsSection = document.getElementById("details");
  const mainPage = document.getElementById("mainPage");
  const searchBar = document.getElementById("searchBar");
  const backBtn = document.getElementById("backBtn");
  const bannerDiv = document.getElementById("deptBanner");
  const advice = document.getElementById("advice");

  // determine nice department name
  const prettyName = {
    health: "Health Science",
    business: "Business",
    agriculture: "Agriculture",
    technology: "Technology"
  }[dept] || dept;

  // show the banner above the white details box with a smooth transition
  bannerDiv.innerHTML = `<h1>Welcome to the department of ${prettyName}</h1>`;
  bannerDiv.style.display = "block";
  // allow CSS transition to run
  requestAnimationFrame(() => bannerDiv.classList.add('visible'));

  detailsSection.innerHTML = departments[dept];
  detailsSection.style.display = "block";
  // let the details box animate in
  requestAnimationFrame(() => detailsSection.classList.add('visible'));

  mainPage.style.display = "none";
  searchBar.style.display = "none";
  // hide the advice panel while viewing a department
  if (advice) advice.style.display = "none";
  backBtn.style.display = "block";

  // Change background for each department
  document.body.className = dept + '-view';
}

function goBack() {
  const detailsSection = document.getElementById("details");
  const mainPage = document.getElementById("mainPage");
  const searchBar = document.getElementById("searchBar");
  const backBtn = document.getElementById("backBtn");
  const bannerDiv = document.getElementById("deptBanner");
  const advice = document.getElementById("advice");

  // animate out banner and details, then clear and restore main page
  bannerDiv.classList.remove('visible');
  detailsSection.classList.remove('visible');

  // wait for the CSS transition to finish, then hide elements
  setTimeout(() => {
    detailsSection.style.display = "none";
    detailsSection.innerHTML = "";

    bannerDiv.style.display = "none";
    bannerDiv.innerHTML = "";

    mainPage.style.display = "flex";
    searchBar.style.display = "block";
    backBtn.style.display = "none";

    // show the advice panel again on the main page
    if (advice) advice.style.display = "block";

    // Reset background
    document.body.className = "";
  }, 300);
}

// keyboard handling for accessible cards
function cardKeydown(e, dept) {
  if (e.key === 'Enter' || e.key === ' ') {
    e.preventDefault();
    showDepartment(dept);
  }
}

function searchCourse() {
let input = document.getElementById("searchBar").value.toLowerCase();
let details = document.getElementById("details");

if (input === "") {
details.innerHTML = "";
details.style.display = "none";
return;
}

let results = "";

for (let key in departments) {
if (departments[key].toLowerCase().includes(input)) {
results += departments[key];
}
}

if (results === "") {
details.innerHTML = "<h2>No results found</h2>";
} else {
details.innerHTML = results;
}

details.style.display = "block";
// animate search results in
requestAnimationFrame(() => details.classList.add('visible'));
}